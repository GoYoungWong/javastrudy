package chapter02;

public class DoubleEx1 {

	public static void main(String[] args) {
		//실수형 데이타타입 : float(4), double(8)
		// 실수형 데이타는 부동소수점(소수점이 이동) 방식
		
		float funm = 3.14f; // float 데이타표현은 마지막에 알파벳 f or F 로 표현.
		double dunm = 3.14; // 3.14D 동일한 표현임.
		
		System.out.println(funm);
		System.out.println(dunm);
	}

}
